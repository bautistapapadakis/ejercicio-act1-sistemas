# Crear lista de frutas
frutas = ["manzana", "banana", "naranja", "pera", "uva", "kiwi"]

# Mostrar algunas frutas específicas
print("Primera fruta:", frutas[0])
print("Última fruta:", frutas[-1])
print("Tercera fruta:", frutas[2])

# Mostrar rebanadas (slices)
print("Primeras 3 frutas:", frutas[:3])
print("Desde la posición 2 hasta la 4:", frutas[2:5])
print("Últimas 2 frutas:", frutas[-2:])